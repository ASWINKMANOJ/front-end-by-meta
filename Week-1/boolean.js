// boolean has two values either true or false
console.log(1==1) //this will log out true
console.log(1!=1) // this will log out false
// single = is assignment operator and == is logical operator
console.log(100=="100") // does not check type of data
console.log(100==="100") // checks the type of data
console.log(1!=="1") // checks the type of data